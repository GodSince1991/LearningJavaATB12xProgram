public class Lab114_While_Loop_False_Condition {
    public static void main(String[] args) {

        int i = 0;
        while (i > 0) {
            /* As the condition is false, there will be no output */
            System.out.println("Enter");
            System.out.println(i);
            i++;
        }
    }
}
